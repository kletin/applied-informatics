import java.lang.String;
public class VariablesDemo {
    void displayVariables() {
        int number = 10;
        double decimal = 5.5;
        char letter = 'A';
        boolean isJavaFun = true;
        String message = "Java είναι καταπληκτική!";

        System.out.println("Ακέραιος: " + number);
        System.out.println("Δεκαδικός: " + decimal);
        System.out.println("Χαρακτήρας: " + letter);
        System.out.println("Boolean: " + isJavaFun);
        System.out.println("Συμβολοσειρά: " + message);
    }
}

